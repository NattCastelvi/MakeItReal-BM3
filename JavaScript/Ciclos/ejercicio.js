//Imprimir los números  del 10 a 20 pero de 2 en 2.

/* 
Inicializar i= 10.
Condición: i menor o igual a 20.
Incrementador sume de a '2'.
Cuerpo: Imprimir en la terminal el valor
*/

for (let i = 10; i <= 20; i = i + 2) {
    console.log("Número: " + i);
}

